#include "so_long.h"

int	ft_down(t_solong **th, int nc, int x, int y)
{
	if ((*th)->map[x + 1][y] == 'E' && !nc)
	{
		(*th)->map[x][y] = '0';
		(*th)->map[++x][y] = 'P';
	}
	else if ((*th)->map[x + 1][y] != '1' && (*th)->map[x + 1][y] != 'E')
	{
		(*th)->map[x][y] = '0';
		(*th)->map[++x][y] = 'P';
	}
	return (x);
}